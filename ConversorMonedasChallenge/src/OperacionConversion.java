public class OperacionConversion {


    public double OperacionConversionMultiplicacion(Double montoRequerido, Double precioMoneda){
        return (montoRequerido * precioMoneda);
    }
    public double OperacionConversionDivision(Double montoRequerido, Double precioMoneda) {
        return (montoRequerido / precioMoneda);
    }


}
